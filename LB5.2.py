import math
r1 = float(input("Введіть значення внутрішнього радіуса: "))
r2 = float(input("Введіть значення зовнішнього радіуса: "))
x1 = float(input("Введіть абсцису точки: "))
y1 = float(input("Введіть ординату точки: "))
if x1 ** 2 + y1 ** 2 == r1 ** 2:
    print("Точка належить внутрішньому кільцю")
elif x1 ** 2 + y1 ** 2 == r2 ** 2:
    print("Точка належить зовнішньому кільцю")
else:
    print("Точка не належить кільцю")
